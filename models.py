from pydantic import BaseModel, Field
from datetime import date as Date
from typing import Optional


class FinNewsArticle(BaseModel):
    """
    Модель для финансовых новостей из российского сегмента.
    Представляет структуру датасета с новостными статьями и их оценкой влияния на рынок.
    """
    
    id: int = Field(
        description="Уникальный идентификатор статьи в датасете"
    )
    
    reasoning: str = Field(
        description="Экспертное обоснование влияния новости на финансовый рынок. "
                   "Объясняет, почему данная новость может повлиять на стоимость акций "
                   "или финансовые показатели компаний/отраслей"
    )
    
    answer: float = Field(
        description="Численная оценка влияния новости на рынок. "
                   "Положительные значения - позитивное влияние на стоимость акций, "
                   "отрицательные - негативное влияние, "
                   "0 - нейтральное влияние"
    )
    
    article_text: str = Field(
        description="Полный текст новостной статьи на русском языке. "
                   "Содержит всю информацию о событии, включая детали, цифры и контекст"
    )
    
    sphere: str = Field(
        description="Сфера/отрасль экономики, к которой относится новость. "
                   "Например: 'Финансы', 'Энергетика', 'Технологии' и т.д."
    )
    
    source: str = Field(
        description="Источник новости - название информационного агентства или издания. "
                   "Например: 'Интерфакс', 'РБК', 'ТАСС'"
    )
    
    date: Date = Field(
        description="Дата публикации новости в формате YYYY-MM-DD"
    )